
// Gallery slider
function startZoom(){
  var zoombg = document.getElementById('black')
  var zoom = document.getElementsByClassName('zoom')

  zoombg.style.display = "block"
  zoom.style.display = "block"
}


function stopZoom(){
  var zoombg = document.getElementById('black')
  var zoom = document.getElementsByClassName('zoom')

  zoombg.style.display = "none"
  zoom.style.display = "none"
}

